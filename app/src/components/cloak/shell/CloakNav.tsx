export function CloakNav() {
  const pathname = usePathname();
  const router = useRouter();
  const { slug, templateId, isLoading, navigatingTo, setNavigatingTo } = useCloakContext();

  // Clear navigatingTo when pathname actually changes (navigation completed)
  useEffect(() => {
    if (navigatingTo && pathname) {
      setNavigatingTo(null);
    }
  }, [pathname]);

  const handleTabClick = useCallback((e: React.MouseEvent, href: string) => {
    e.preventDefault();
    if (href === pathname) return; // already on this tab
    setNavigatingTo(href);
    router.push(href);
  }, [pathname, setNavigatingTo, router]);

  if (isLoading) {
    return (
      <nav className="bg-card border-b border-border px-6 py-2 animate-shimmer">
        <div className="flex gap-2">
          {[1, 2, 3, 4].map((i) => (
            <div key={i} className="h-10 w-24 bg-background-tertiary rounded-md" />
          ))}
        </div>
      </nav>
    );
  }

  const navItemIds = TEMPLATE_NAV_CONFIG[templateId] || ['overview', 'proposals', 'members', 'settings'];

  const navItems: NavItem[] = navItemIds.map((id) => {
    const item = NAV_ITEMS[id];
    const href = id === 'overview' ? `/cloak/${slug}` : `/cloak/${slug}/${id}`;
    return {
      id,
      label: item.label,
      href,
      icon: item.icon,
    };
  });

  const isActive = (href: string) => {
    // If navigating, highlight the target tab immediately
    if (navigatingTo) {
      return href === navigatingTo;
    }
    if (href === `/cloak/${slug}`) {
      return pathname === href;
    }
    return pathname?.startsWith(href);
  };

  return (
    <nav className="bg-card border-b border-border">
      <div className="px-6 overflow-x-auto">
        <div className="flex gap-1 -mb-px">
          {navItems.map((item) => (
            <a
              key={item.id}
              href={item.href}
              onClick={(e) => handleTabClick(e, item.href)}
              className={`relative flex items-center gap-2 px-4 py-3 text-sm font-medium border-b-2 border-transparent transition-colors whitespace-nowrap cursor-pointer ${
                isActive(item.href)
                  ? 'text-accent'
                  : 'text-foreground-muted hover:text-foreground-secondary'
              }`}
            >
              {item.icon}
              {item.label}
              {isActive(item.href) && (
                <motion.div
                  className="absolute bottom-0 left-0 right-0 h-0.5 bg-accent"
                  layoutId="nav-indicator"
                  transition={{ type: 'spring', stiffness: 380, damping: 30 }}
                />
              )}
            </a>
          ))}
        </div>
      </div>
    </nav>
  );
}
